# Dendrogram suggest 2 clusters
n_clusters = 2
hierarchy_cluster = AgglomerativeClustering(n_clusters = n_clusters,linkage = 'ward')
hierarchy_cluster.fit_predict(df)
df['hierarchy_cluster'] = hierarchy_cluster.labels_ # Storing the clusters obtained from the AgglomerativeClustering into the dataframe
print(f"Done Applying Agglomerative clustering with {n_clusters} clusters")
df.head()

from sklearn.cluster import DBSCAN
from sklearn.neighbors import NearestNeighbors
import numpy as np

# Selecting the Minpoints
# Minpoints are calculated using 2 * total_no_of_features
min_samples = 22 # As in our dataset there are 11 features
neighbors = NearestNeighbors(n_neighbors = min_samples) # Nearest neighbour is a tool used to find the nearest neighbors from a data point within the epsilon
neighbors_fit = neighbors.fit(df)
distances,indices = neighbors_fit.kneighbors(df) # here i am finding the k neighbors that is 22 neighbors with respect to the data point and getting distance and indices
distances = np.sort(distances[:, min_samples-1],axis = 0) #here from distance table i am getting all the 22nd neighbors distance of each data point
# Plotting the Epsilon
plt.figure(figsize = (10,6))
plt.plot(distances)
plt.title('Epsion plot')
plt.xlabel('Data points')
plt.ylabel('Distance')
plt.grid(True)
plt.show()

# From  the epsion plot 1.8 is the value where there is a curve(elbow).Hence 2.5 is the best epsilon value
epsilon = 1.8
min_samples = 22
dbscan = DBSCAN(eps = 1.8,min_samples = min_samples)
cluster_dbscan = dbscan.fit_predict(df)
# Sihouette score will only work only when there are two or more clusters so hence implementing if and else block to check whether DBSCAN has more than one cluster
from sklearn.metrics import silhouette_score
if len(set(cluster_dbscan)  - {-1}) > 1: # set will give unique values and not selecting -1 which are noise points >1 answers the question of whether are more than one cluster
  score_dbscan = silhouette_score(df[cluster_dbscan != -1],cluster_dbscan[cluster_dbscan !=-1])#here i am matching the dataframe and the labels in the cluster_dbscan col
  print(f'For epsilon value of {epsilon} and {min_samples} min_samples the silhouette score is {score_dbscan:.4f}')
else:
  print('There are no clusters more than one in DBCSAN Algorithm')

# Storing the labels of clusters obtained using DBSCAN Algorithm
df['cluster_dbscan'] = cluster_dbscan

df['cluster_dbscan'].value_counts()

