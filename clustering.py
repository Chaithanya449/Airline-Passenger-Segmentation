# K - Means clustering
from sklearn.cluster import KMeans
inertia_values = []
k_range = range(1,11)
for k in k_range:
    kmeans = KMeans(n_clusters = k,random_state = 42)
    kmeans.fit(df)
    inertia_values.append(kmeans.inertia_)

# Plotting the elbow curve for optimal K
plt.figure(figsize = (10,6))
plt.plot(k_range,inertia_values,marker = 'o',linestyle= '--')
plt.title('Elbow curve')
plt.xlabel('Number of clusters')
plt.ylabel('Inertia_values')
plt.xticks(k_range)
plt.grid(True)
plt.show()

# From Elbow curve 4 is the optimal number of cluster required for this analysis
Optimal_k = 4
# Applying optimal_k onto the kmeans clustering
kmeans = KMeans(n_clusters = Optimal_k,random_state = 42)
k_cluster=kmeans.fit_predict(df)
df['k_cluster'] = k_cluster # cluster variable contains all the cluster values from 1 to 10 it shows which passenger lies in which cluster
from sklearn.metrics import silhouette_score
silhouette_score = silhouette_score(df, k_cluster)
print(f'for k = {Optimal_k} the silhouette score is {silhouette_score}')
df.head()

"""Silhouette score: It is a score that tells whether the data points are perfectly assigned  to the clusters
>0.5 is a strong cluster arrangement which data points are well arranged in the clusters.
<0.5 is a weak cluster arrangement
silhouette score = '0' or near to '0' means the clusters will overlap eachother.
for k = 4 the silhouette score is 0.27604149683953577 here the clusters are overlapped in this case if the no of clusters are increased that is at k=10 the score is 0.45 which is better than the score of k=4. Here Elbow curve said that k=4  is the correct no of cluisters and silhouette score tells that increase the number of clusters so that clusters are separated very well. Here we might think to increase the number of clusters but if no of clusters selected are 10 then the plot becomes messy the results cannot be interpreted and data is not uderstood well. So that is the reason Elbow curve is the best choice to obtain optimal k

"""

import scipy
import scipy.cluster.hierarchy as sch # To Build a dendogram
from sklearn.cluster import AgglomerativeClustering

print("Creating a dendrogram to check the no of clusters needed")
